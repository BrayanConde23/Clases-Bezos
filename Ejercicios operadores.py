# Operadores sesion 3.

num1 = int (input("Ingrese un numero entero: "))
num2 = int (input("Ingrese otro numero entero: "))

# 1. Suma
result = num1 + num2 
print("La suma de los numeros enteros es:",result)

# 2. Resta
result = num2 - num1
print("La resta del segundo numero entero al primero es:", result)

# 3. Multiplicacion
result = num1 * num2
print("La multiplicacion de los numeros enteros es:", result)

# 4. Division
result = num1 / num2
print("La division de los dos numeros enteros es:", result)

# 5. Division entera 
result = num2 // num1
print("La division entera de los dos numeros enteros es:", result)

# 6. Modulo
result = num2 % num1
print("El modulo de los dos numeros enteros es:", result)

# 7. Exponenciacion
result = num1 ** num2
print("La exponenciacion de los dos numeros enteros es:", result)

# 8. Comprobacion de igualdad
result = num1 == num2
print(result) 

# 9. Comprobacion de desigualdad
result = num1 != num2
print(result) 

# 10. Comprobación de si el primer número es menor que el segundo
result = num1 < num2
print(result)

# 11. Comprobación de si el primer número es mayor que el segundo
result = num1 > num2
print(result)

# 12. Comprobación de si el primer número es menor o igual que el segundo
result = num1 <= num2
print(result)

# 13. Comprobación de si el primer número es mayor o igual que el segundo
result = num1 >= num2
print(result)

