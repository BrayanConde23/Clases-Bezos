""" RANGE """
# Lista de numeros del 10 al 30 que muestre pares e impares.
pares = list(range(10,31,2))

print(pares)

impares = list(range(11,30,2))

print(impares)

# Multiplos de 4 del 10 al 40
multiplos = list(range(12,41,4))
print(multiplos) 

